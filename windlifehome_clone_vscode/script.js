const whatsappUrl = "https://wa.me/5585999999999";

function getNestedTranslation(obj, path) {
  return path.split(".").reduce((acc, key) => acc && acc[key], obj);
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.es;

  document.documentElement.lang = lang === "pt" ? "pt-BR" : lang;
  localStorage.setItem("windlife_lang", lang);

  const pageTitle = getNestedTranslation(dict, "meta.title");
  if (pageTitle) document.title = pageTitle;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = getNestedTranslation(dict, key);
    if (value) {
      const icon = el.getAttribute("data-icon");
      if (icon) {
        el.innerHTML = `<span class="ui-icon icon-${icon}" aria-hidden="true"></span> ${value}`;
      } else {
        el.textContent = value;
      }
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const value = getNestedTranslation(dict, key);
    if (value) el.innerHTML = value;
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    const value = getNestedTranslation(dict, key);
    if (value) el.setAttribute("aria-label", value);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const key = el.getAttribute("data-i18n-title");
    const value = getNestedTranslation(dict, key);
    if (value) el.setAttribute("title", value);
  });

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", String(btn.dataset.lang === lang));
  });
}

function setupLanguageSwitcher() {
  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.addEventListener("click", () => {
      applyLanguage(btn.dataset.lang);
    });
  });

  const savedLang = localStorage.getItem("windlife_lang") || "es";
  applyLanguage(savedLang);
}

function closeMobileMenu() {
  document.body.classList.remove("nav-open");
  document.querySelector(".nav-toggle")?.setAttribute("aria-expanded", "false");
}

function scrollToTarget(selector) {
  const target = document.querySelector(selector);
  if (!target) return;
  closeMobileMenu();
  target.scrollIntoView({ behavior: "smooth", block: "start" });
}

function switchTab(el, type) {
  document.querySelectorAll(".booking-tab").forEach(tab => tab.classList.remove("active"));
  el.classList.add("active");

  const submit = document.querySelector(".booking-submit");
  if (!submit) return;
  if (type === "kite") submit.dataset.bookingType = "booking.kiteStay";
  if (type === "clase") submit.dataset.bookingType = "booking.classesOnly";
  if (type === "home-section") submit.dataset.bookingType = "booking.stay";
}

function setupInteractions() {
  const navToggle = document.querySelector(".nav-toggle");
  const navMenu = document.querySelector(".nav-menu");

  navToggle?.addEventListener("click", () => {
    const isOpen = document.body.classList.toggle("nav-open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navMenu?.querySelectorAll("a, button").forEach(item => {
    item.addEventListener("click", event => {
      if (event.currentTarget.hasAttribute("data-lang")) return;
      closeMobileMenu();
    });
  });

  document.addEventListener("keydown", event => {
    if (event.key === "Escape") closeMobileMenu();
  });

  document.addEventListener("click", event => {
    if (document.body.classList.contains("nav-open")) {
      if (!navMenu?.contains(event.target) && !navToggle?.contains(event.target)) closeMobileMenu();
    }

    const scrollButton = event.target.closest("[data-scroll]");
    if (scrollButton) {
      event.preventDefault();
      scrollToTarget(scrollButton.dataset.scroll);
    }
  });

  document.querySelectorAll(".guest-btn").forEach(btn => {
    btn.addEventListener("click", () => {
      const box = btn.closest(".guest-box");
      const num = box?.querySelector(".guest-num");
      if (!num) return;
      let val = parseInt(num.textContent, 10);
      if (btn.textContent.trim() === "+") val = Math.min(val + 1, 10);
      if (btn.textContent.trim() === "−") val = Math.max(val - 1, 0);
      num.textContent = val;
    });
  });

  document.querySelectorAll(".room-option").forEach(opt => {
    opt.addEventListener("click", () => {
      document.querySelectorAll(".room-option").forEach(option => option.classList.remove("selected"));
      opt.classList.add("selected");
      const input = opt.querySelector("input[type=radio]");
      if (input) input.checked = true;
    });
  });

  document.querySelectorAll(".room-card, .kite-card").forEach(card => {
    card.addEventListener("click", event => {
      if (event.target.closest("button, a, input, label")) return;
      card.querySelector("button")?.click();
    });
  });

  document.querySelectorAll(".kite-book-btn").forEach(button => {
    button.addEventListener("click", () => {
      const lang = localStorage.getItem("windlife_lang") || "es";
      const message = getNestedTranslation(translations[lang] || translations.es, "whatsapp.classMessage");
      window.open(`${whatsappUrl}?text=${encodeURIComponent(message)}`, "_blank", "noopener");
    });
  });

  document.querySelector(".booking-submit")?.addEventListener("click", event => {
    const lang = localStorage.getItem("windlife_lang") || "es";
    const dict = translations[lang] || translations.es;
    const prefix = getNestedTranslation(dict, "whatsapp.availabilityMessage");
    const typeKey = event.currentTarget.dataset.bookingType || "booking.stay";
    const type = getNestedTranslation(dict, typeKey) || typeKey;
    window.open(`${whatsappUrl}?text=${encodeURIComponent(`${prefix} ${type}`)}`, "_blank", "noopener");
  });

  const checkIn = document.querySelector("input[type=date]:first-of-type");
  const checkOut = document.querySelector("input[type=date]:last-of-type");
  const nightsDisplay = document.querySelector(".guest-box:last-child .guest-num");

  function calcNights() {
    if (checkIn?.value && checkOut?.value && nightsDisplay) {
      const diff = (new Date(checkOut.value) - new Date(checkIn.value)) / (1000 * 60 * 60 * 24);
      if (diff > 0) nightsDisplay.textContent = diff;
    }
  }

  checkIn?.addEventListener("change", calcNights);
  checkOut?.addEventListener("change", calcNights);

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = "1";
        entry.target.style.transform = "translateY(0)";
      }
    });
  }, { threshold: 0.1 });

  document.querySelectorAll(".room-card, .kite-card, .pricing-card, .pillar").forEach(el => {
    el.style.opacity = "0";
    el.style.transform = "translateY(24px)";
    el.style.transition = "opacity 0.6s ease, transform 0.6s ease";
    observer.observe(el);
  });
}

document.addEventListener("DOMContentLoaded", () => {
  setupLanguageSwitcher();
  setupInteractions();
});
